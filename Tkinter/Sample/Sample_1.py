import tkinter as tk
from tkinter import ttk
# ! -----------------------------------------------------------------------------------------------------------------------------------------------


# ^ -----------------  ROOT    ------------------------------------------------------------------------------
root = tk.Tk()
root.title("My Tkinter Project")
root.geometry("800x400")

# ! ----------------------------------------------------------------------------------------------------------------------------

# & -----------------   Menu       ------------------------------------------------------------------------------

menu = tk.Menu(root)
root.configure(menu=menu)

# ? ----------------------------------------------------------------------------------------------------------------------------

# & -----------------   Sub Menu 1       ------------------------------------------------------------------------------

fileMenu1 = tk.Menu(menu)
fileMenu1.add_command(label="New")
fileMenu1.add_command(label="Open")
fileMenu1.add_command(label="Save")
fileMenu1.add_command(label="Save As")
fileMenu1.add_command(label="Delete")
menu.add_cascade(label="Data",command=lambda: print())


# & -----------------   ENTRY       ------------------------------------------------------------------------------
# & -----------------   ENTRY       ------------------------------------------------------------------------------
# & -----------------   ENTRY       ------------------------------------------------------------------------------
# & -----------------   ENTRY       ------------------------------------------------------------------------------
# & -----------------   ENTRY       ------------------------------------------------------------------------------
# & -----------------   ENTRY       ------------------------------------------------------------------------------
# & -----------------   ENTRY       ------------------------------------------------------------------------------


root.mainloop()

