import tkinter as tk
from tkinter import ttk
# ! -----------------------------------------------------------------------------------------------------------------------------------------------


# ^ -----------------  ROOT    ------------------------------------------------------------------------------
root = tk.Tk()
root.title("My Tkinter Project")
root.geometry("800x400")

# ! ----------------------------------------------------------------------------------------------------------------------------

# ^ -----------------   Menu       ------------------------------------------------------------------------------

menu = tk.Menu(root)
root.configure(menu=menu)

# ! ----------------------------------------------------------------------------------------------------------------------------

# ^ -----------------   Sub Menu 1       ------------------------------------------------------------------------------

fileMenu1 = tk.Menu(menu,tearoff=False)
fileMenu1.add_command(label="New",command=lambda:print("New File"))
fileMenu1.add_command(label="Open",command=lambda:print("Open File"))
fileMenu1.add_command(label="Save",command=lambda:print("Saved"))
fileMenu1.add_command(label="Save As",command=lambda:print("Saved As"))
fileMenu1.add_command(label="Delete",command=lambda:print("Delete"))
menu.add_cascade(label="File",menu=fileMenu1,command=lambda: print("Data Clicked"))

# ! ----------------------------------------------------------------------------------------------------------------------------


# ^ -----------------   Help Menu      ------------------------------------------------------------------------------

help = tk.Menu(menu,tearoff=False)
help.add_command(label="Visit Our Website",command=lambda:print("Visit Our Website"))
menu.add_cascade(label="Help",menu=help,command=lambda:print("Help"))

# ^ -----------------   Button Menu       ------------------------------------------------------------------------------

menu_btn = ttk.Menubutton(root,text="Menu_Button")
menu_btn.grid()

btnSubMenu = tk.Menu(menu_btn,tearoff=False)
btnSubMenu.add_command(label='Entry 1',command=lambda : print("Entry 1"))
menu_btn.configure(menu = btnSubMenu)

# ! ----------------------------------------------------------------------------------------------------------------------------
# ! ----------------------------------------------------------------------------------------------------------------------------
# ! ----------------------------------------------------------------------------------------------------------------------------
# ! ----------------------------------------------------------------------------------------------------------------------------
# ! ----------------------------------------------------------------------------------------------------------------------------
# ! ----------------------------------------------------------------------------------------------------------------------------
# ! ----------------------------------------------------------------------------------------------------------------------------

# ^ -----------------   ENTRY       ------------------------------------------------------------------------------
# ^ -----------------   ENTRY       ------------------------------------------------------------------------------
# ^ -----------------   ENTRY       ------------------------------------------------------------------------------
# ^ -----------------   ENTRY       ------------------------------------------------------------------------------
# ^ -----------------   ENTRY       ------------------------------------------------------------------------------
# ^ -----------------   ENTRY       ------------------------------------------------------------------------------


root.mainloop()

